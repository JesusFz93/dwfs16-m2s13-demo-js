// Bucle for
console.log("Bucle for");
for (let i = 0; i < 10; i++) {
  console.log(i);
}

// Bucle while
console.log("Bucle while");
let i = 0;
while (i < 10) {
  console.log(i);
  i++;
}

// Bucle for of
console.log("Bucle for of");
const colores = ["rojo", "verde", "azul"];

for (let color of colores) {
  console.log(color);
}
