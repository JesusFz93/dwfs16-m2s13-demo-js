const a = 7;
const b = 3;

// Operadores aritméticos
console.log(a + b);
console.log(a - b);
console.log(a * b);
console.log(a / b);
console.log(a % b);

// Operadores de asignación
let c = 7;
c += 3;
console.log(c);

// Operadores de comparación
console.log(a == b);
console.log(a != b);
console.log(a > b);
console.log(a < b);
console.log(a >= b);
console.log(a <= b);

// Operadores lógicos
console.log(a > b && a != b);
console.log(a > b || a != b);
console.log(!(a > b));

// Operadores de incremento y decremento
let d = 7;
console.log(d++);
console.log(d);
console.log(++d);
console.log(d);
console.log(d--);
console.log(d);
console.log(--d);
console.log(d);

// Operadores de concatenación
let nombre = "Jesus";
let apellido = "Fernandez";
console.log(nombre + " " + apellido);

// Operadores ternarios
let edad = 30;
let activo = true;
console.log(edad >= 18 ? "Es mayor de edad" : "Es menor de edad");
console.log(activo ? "Activo" : "Inactivo");

// Operadores de igualdad
console.log(1 == 1);
console.log(1 == "1");
console.log(1 === "1");
console.log(1 === 1);
console.log(1 != 1);
console.log(1 != "1");
console.log(1 !== "1");
console.log(1 !== 1);
